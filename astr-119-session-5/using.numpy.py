import numpy as np   # import numpy library

x = 1.0   # define two floats
y = 2.0

# trig functions

print(np.sin(x))		# sinx
print(np.cos(x))		# cosx
print(np.tan(x))		# tanx
print(np.arcsin(x))		# arcsinx
print(np.arccos(x))		# arccosx
print(np.arctan(x))		# arctanx
print(np.arctan2(x,y))	# arctan(x/y)
print(np.rad2deg(x))	# convert from rad to deg

# Hyperbolic functions

print(np.sinh(x))		# sinhx
print(np.cosh(x))		# coshx
print(np.tanh(x))		# tahnx
print(np.arcsinh(x))	# arcsinhx
print(np.arccosh(x))	# arccoshx
print(np.arctanh(x))	# arctanhx
