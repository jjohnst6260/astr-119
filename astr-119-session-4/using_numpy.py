import numpy as np 

x = 1.0   # defines a floating point #
y = 2.0   # another float

# trig

print(np.sin(x))   		# prints sinx

print(np.cos(x))		# prints cosx

print(np.tan(x))		# prints tanx

print(np.arcsin(x))		# prints arcsinx

print(np.arccos(x))		# prints arccosx

print(np.arctan(x))		# prints arctanx

print(np.arctan2(x,y))	# prints arctan(x/y)

print(np.rad2deg(x))	# conversion from radians to degrees

# hyperbolic functions

print(np.sinh(x))		# prints sinhx

print(np.cosh(x))		# prints coshx

print(np.tanh(x))		# prints tanhx

print(np.arcsinh(x))	# prints arcsinhx

print(np.arccosh(x))	# prints arccoshx

print(np.arctanh(x))	# prints arctanhx
